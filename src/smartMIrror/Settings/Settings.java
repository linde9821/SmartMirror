package smartMIrror.Settings;

public class Settings {
	int x, y;
	
	public Settings() {
		x = 540;
		y = 960;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
	
	
}
